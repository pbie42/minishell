#include "minishell.h"

int						lsh_cd(char **args)
{
	if (args[1] == NULL)
		ft_putendl("lsh: expected arguments to \"cd\"");
	else
	{
		if (chdir(args[1]) != 0)
			ft_exit("lsh");
	}
	return (1);
}