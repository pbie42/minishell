#include "minishell.h"

int						alpha_check(char *s)
{
	int					i;

	i = -1;
	while (s[++i])
		if (s[i] != ' ' && s[i] != '\t' && s[i] != '\r' && s[i] != '\v'
		&& s[i] != '\f' && s[i] != '\n')
			return (1);
	return (0);
}