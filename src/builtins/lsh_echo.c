#include "minishell.h"

int					lsh_echo(char **args)
{
	int				i;

	i = 0;
	while (args[++i] != NULL)
	{
		ft_putstr(args[i]);
		ft_putchar(' ');
	}
	ft_putchar('\n');
	return (1);
}