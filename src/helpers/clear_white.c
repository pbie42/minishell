#include "minishell.h"

void						clear_white(char *s)
{
	int						i;
	char					second;

	i = -1;
	while (s[++i])
		if (s[i] == '\t' || s[i] == '\r' || s[i] == '\v'
		|| s[i] == '\f' || s[i] == '\n')
		{
			second = ' ';
			char_swap(&s[i], &second);
			second = ' ';
		}
}
